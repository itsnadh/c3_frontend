import axios from "axios";
import { ref } from "vue";

const field = ref({
  user_name: "",
  user_email: "",
  user_password: "",
});

const register = () => {
  axios
    .post(`${import.meta.env.VITE_APP_BASE_URL}/user`, field.value)
    .then((res) => {
      window.location.href = "/";
    })
    .catch((err) => console.log(err));
};
